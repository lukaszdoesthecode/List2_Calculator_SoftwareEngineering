public class Calculator {
    /** Function adding two floating-point numbers
     *
     * @param a - fist parameter (float)
     * @param b - second parameter (float)
     * @return - sum of both numbers
     */
    public float add(float a, float b){
        return a+b;
    }

    /** Function subtracting two floating-point numbers
     *
     * @param a - fist parameter (float)
     * @param b - second parameter (float)
     * @return - sum of both numbers
     */
    public float sub(float a, float b){
        return a-b;
    }

    /** Function multiplying two numbers (double type).
     *
     * @param a - fist parameter (double)
     * @param b - second parameter (double)
     * @return - sum of both numbers
     */
    public double mul(double a, double b){
        return a*b;
    }
}
